.MODEL SMALL
.STACK 100h

.DATA    
ST DB 'Enter a: $'
STT DB 0AH,0DH,'Enter b: $'
STTT DB 0AH,0DH,'Larger: $' 
ST1 DB 0AH,0DH, 'Smaller: $'
A DB ?
B DB ?



.CODE
PROC MAIN
    
    
MOV AX, @DATA
MOV DS,AX

LEA DX,ST
MOV AH,9
INT 21H

MOV AH,1
INT 21h
MOV A,AL

LEA DX,STT
MOV AH,9
INT 21H

MOV AH,1
INT 21h
MOV B,AL

MOV BL,B
CMP BL,A
JG SMALL
JL BIG  



SMALL:
LEA DX,ST1
MOV AH,9
INT 21H

MOV DL,A
MOV AH,2
INT 21h

LEA DX,STTT
MOV AH,9
INT 21H

MOV DL,B
MOV AH,2
INT 21h
JMP END 





BIG:
LEA DX,ST1
MOV AH,9
INT 21H

MOV DL,B
MOV AH,2
INT 21h

LEA DX,STTT
MOV AH,9
INT 21H

MOV DL,A
MOV AH,2
INT 21h 






END:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN